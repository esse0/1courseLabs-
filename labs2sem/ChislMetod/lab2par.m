x=[10, 11, 13, 16, 18, 20, 21, 25, 28, 30];
y=[3.3, 4, 5.1, 7, 11, 13, 14, 15, 17, 18];
n = 10;
T = linspace(10,30);
y = x.^2;
A = [sum(x.^4),sum(x.^3),sum(x.^2); sum(x.^3),sum(x.^2), sum(x); sum(x.^2), sum(x), n];
B = [sum((x.^2).*y);sum(x.*y);sum(y)];
X = inv(A)*B;
y1= @(x) X(3,1)+X(2,1).*x+X(1,1).*x.^2;
plot(x,y,T, y1(T));
grid on;